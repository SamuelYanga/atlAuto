package com.objectivasoftware.atl.core.vo;

public class ProductTotalInfo {

	private String bv;
	private String pv;
	private String amount;

	public String getBv() {
		return bv;
	}

	public void setBv(String bv) {
		this.bv = bv;
	}

	public String getPv() {
		return pv;
	}

	public void setPv(String pv) {
		this.pv = pv;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

}
